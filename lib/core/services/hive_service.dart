import 'package:hive_flutter/hive_flutter.dart';
import 'package:flutter_secure_storage/flutter_secure_storage.dart';
import 'package:hilway/core/constants/app_constants.dart';
import 'package:hilway/core/models/mood_log.dart';
import 'package:hilway/core/models/stress_rating.dart';
import 'package:hilway/core/models/chat_message.dart';
import 'package:hilway/core/models/chat_session.dart';
import 'package:hilway/core/models/planner_entry.dart';
import 'package:hilway/core/models/assessment_result.dart';
import 'package:hilway/core/models/journal_entry.dart';
import 'package:hilway/clinical_duty/models/shift_task.dart';
import 'package:hilway/core/models/refuel_log.dart';
import 'package:hilway/core/models/sync_job.dart';

class HiveService {
  HiveService._();

  static const _encryptionKeyName = 'hive_encryption_key';
  static const _secureStorage = FlutterSecureStorage(
    aOptions: AndroidOptions(encryptedSharedPreferences: true),
  );

  static Future<void> init() async {
    await Hive.initFlutter();

    // Register all Hive adapters (generated by build_runner)
    Hive.registerAdapter(MoodLogAdapter());
    Hive.registerAdapter(StressRatingAdapter());
    Hive.registerAdapter(ChatMessageAdapter());
    Hive.registerAdapter(ChatSessionAdapter());
    Hive.registerAdapter(PlannerEntryAdapter());
    Hive.registerAdapter(AssessmentResultAdapter());
    Hive.registerAdapter(JournalEntryAdapter());
    Hive.registerAdapter(ShiftTaskAdapter());
    Hive.registerAdapter(RefuelLogAdapter());
    Hive.registerAdapter(SyncStateAdapter());
    Hive.registerAdapter(SyncJobAdapter());

    final encryptionKey = await _getOrCreateEncryptionKey();
    await _openBoxes(encryptionKey);
  }

  static Future<List<int>> _getOrCreateEncryptionKey() async {
    final existingKey = await _secureStorage.read(key: _encryptionKeyName);

    if (existingKey != null) {
      return existingKey.codeUnits;
    }

    final newKey = Hive.generateSecureKey();
    await _secureStorage.write(
      key: _encryptionKeyName,
      value: String.fromCharCodes(newKey),
    );
    return newKey;
  }

  static Future<void> _openBoxes(List<int> encryptionKey) async {
    final cipher = HiveAesCipher(encryptionKey);

    // Encrypted boxes — HIGH sensitivity (chat, journal/settings)
    await Hive.openBox<ChatMessage>(
      AppConstants.boxChatMessages,
      encryptionCipher: cipher,
    );
    await Hive.openBox<ChatSession>(
      AppConstants.boxChatSessions,
      encryptionCipher: cipher,
    );
    await Hive.openBox<JournalEntry>(
      AppConstants.boxJournalEntries,
      encryptionCipher: cipher,
    );
    await Hive.openBox(
      AppConstants.boxSettings,
      encryptionCipher: cipher,
    );

    // Unencrypted boxes — MEDIUM/LOW sensitivity
    await Hive.openBox<MoodLog>(AppConstants.boxMoodLogs);
    await Hive.openBox<StressRating>(AppConstants.boxStressRatings);
    await Hive.openBox<PlannerEntry>(AppConstants.boxPlannerEntries);
    await Hive.openBox<AssessmentResult>(AppConstants.boxAssessments);
    await Hive.openBox<ShiftTask>(AppConstants.boxShiftTasks);
    await Hive.openBox<RefuelLog>(AppConstants.boxRefuelLogs);
    await Hive.openBox<SyncJob>(AppConstants.boxSyncQueue);
    await Hive.openBox(AppConstants.boxUserCache);
  }

  // ── Box accessors ─────────────────────────────────────────────────────────
  static Box<MoodLog> get moodBox =>
      Hive.box<MoodLog>(AppConstants.boxMoodLogs);

  static Box<StressRating> get stressBox =>
      Hive.box<StressRating>(AppConstants.boxStressRatings);

  static Box<ChatMessage> get chatBox =>
      Hive.box<ChatMessage>(AppConstants.boxChatMessages);

  static Box<ChatSession> get chatSessionBox =>
      Hive.box<ChatSession>(AppConstants.boxChatSessions);

  static Box<PlannerEntry> get plannerBox =>
      Hive.box<PlannerEntry>(AppConstants.boxPlannerEntries);

  static Box<AssessmentResult> get assessmentBox =>
      Hive.box<AssessmentResult>(AppConstants.boxAssessments);

  static Box<JournalEntry> get journalBox =>
      Hive.box<JournalEntry>(AppConstants.boxJournalEntries);

  static Box<ShiftTask> get shiftBox =>
      Hive.box<ShiftTask>(AppConstants.boxShiftTasks);

  static Box<RefuelLog> get refuelBox =>
      Hive.box<RefuelLog>(AppConstants.boxRefuelLogs);

  static Box get settingsBox =>
      Hive.box(AppConstants.boxSettings);

  static Box get userCacheBox =>
      Hive.box(AppConstants.boxUserCache);

  // ── Generic helpers ───────────────────────────────────────────────────────
  static Box getBox(String boxName) => Hive.box(boxName);

  static Future<void> clearBox(String boxName) async {
    await Hive.box(boxName).clear();
  }

  static Future<void> closeAll() async {
    await Hive.close();
  }

  static Future<void> clearAllData() async {
    for (final boxName in [
      AppConstants.boxMoodLogs,
      AppConstants.boxStressRatings,
      AppConstants.boxChatMessages,
      AppConstants.boxChatSessions,
      AppConstants.boxJournalEntries,
      AppConstants.boxPlannerEntries,
      AppConstants.boxAssessments,
      AppConstants.boxShiftTasks,
      AppConstants.boxRefuelLogs,
      AppConstants.boxSyncQueue,
      AppConstants.boxSettings,
      AppConstants.boxUserCache,
    ]) {
      if (Hive.isBoxOpen(boxName)) {
        await Hive.box(boxName).clear();
      }
    }
  }

  static Future<void> deleteAllData() async {
    for (final boxName in [
      AppConstants.boxMoodLogs,
      AppConstants.boxStressRatings,
      AppConstants.boxChatMessages,
      AppConstants.boxChatSessions,
      AppConstants.boxJournalEntries,
      AppConstants.boxPlannerEntries,
      AppConstants.boxAssessments,
      AppConstants.boxSettings,
      AppConstants.boxUserCache,
    ]) {
      if (Hive.isBoxOpen(boxName)) {
        await Hive.box(boxName).deleteFromDisk();
      }
    }
    await _secureStorage.delete(key: _encryptionKeyName);
  }
}